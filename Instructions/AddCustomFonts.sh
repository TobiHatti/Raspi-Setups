# Add custom fonts from OTF or TTF-Files to the Raspberry Pi (running raspian)

# Step 1) Upload your .otf / .ttf files to the pi
# e.g. to /home/pi/myFonts/myCustomFont.ttf

# Step 2) Copy to fonts-folder
sudo cp /home/pi/myFonts/myCustomFont.ttf /usr/local/share/fonts/

# Step 3) Reboot the system.
sudo reboot

# Done.
# The font can now be used by the Pi